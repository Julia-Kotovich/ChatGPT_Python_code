# Initializing a queue
queue = []

# Adding an element to the queue
queue.append(1)
queue.append(2)
queue.append(3)
print("The current queue is:", queue)

# Removing an element from the queue (FIFO)
queue.pop(0)
print("The updated queue is:", queue)

# Accessing the front element of the queue
print("The front element is:", queue[0])

# Finding the size of the queue
size = len(queue)
print("The size of the queue is:", size)
