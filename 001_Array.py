# Initializing an array
arr = [1, 2, 3, 4, 5]

# Accessing an element of the array
print("The first element is:", arr[0])

# Modifying an element of the array
arr[0] = 6
print("The modified array is:", arr)

# Appending an element to the array
arr.append(7)
print("The updated array is:", arr)

# Removing an element from the array
arr.remove(6)
print("The updated array is:", arr)

# Sorting the array
arr.sort()
print("The sorted array is:", arr)

# Finding the length of the array
length = len(arr)
print("The length of the array is:", length)
